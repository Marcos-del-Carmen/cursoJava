package Denisse;

public class act19_tablaMul {
    public static void main(String[] args) {
       
        for (int numTabla = 1; numTabla <= 10; numTabla++) {
            for(int num = 1; num <= 10; num++){
                System.out.println(numTabla+"x"+num+"="+numTabla*num);
            }
            System.out.println("");
        }
    }
    
}
