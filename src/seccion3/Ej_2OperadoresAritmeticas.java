package seccion3;

public class Ej_2OperadoresAritmeticas {
    public static void main(String [] args) {
        int n1 = 5;
        int n2 = 8;
        
        double div = n1 / n2
               ,suma = n1 + n2
               ,resta = n1 - n2
               ,mul = n1 * n2;
        
        System.out.println("Dividición: " + div);
        System.out.println("Suma: " + suma);
        System.out.println("Resta: " + resta);
        System.out.println("Multiplicación: " + mul);
    }
}
